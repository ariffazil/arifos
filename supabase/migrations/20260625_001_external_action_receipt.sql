-- ── 1. Create the table ─────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS public.external_action_receipt (
  id                       bigint PRIMARY KEY GENERATED ALWAYS AS IDENTITY,

  -- Identity
  receipt_id               uuid NOT NULL UNIQUE DEFAULT gen_random_uuid(),
  source_system            text NOT NULL,                 -- 'hostinger' | 'cloudflare' | 'github' | 'local_fs' | 'docker' | 'cron' | 'shell' | ...
  source_subdomain         text,                          -- e.g. 'vps:af-forge', 'dns:arif-fazil.com', 'repo:arifOS', 'path:/root/...'

  -- Action
  action_type              text NOT NULL,                 -- 'vps_restart', 'dns_update', 'pr_merge', 'file_write', 'secret_rotate', etc.
  target                   text NOT NULL,                 -- resource affected (vps_id, zone, repo, file path)
  parameters               jsonb NOT NULL DEFAULT '{}'::jsonb,
  result                   text NOT NULL DEFAULT 'pending'
                           CHECK (result IN ('pending','success','failure','partial','rolled_back')),
  error_message            text,
  external_reference       text,                          -- ID returned by external system (VPS job ID, PR number, commit SHA, etc.)

  -- Provenance / chain
  tool_call_id             text,                          -- back-link to arifosmcp_tool_calls if generated by a tool
  session_id               text,
  trace_id                 text,
  actor_id                 text NOT NULL,                 -- who initiated (agent:openclaw, human:arif, system:cron, ...)
  initiated_by_ref         text,                          -- chain back to actor

  -- Constitutional governance (per doctrine §3.5)
  risk_tier                integer NOT NULL DEFAULT 1
                           CHECK (risk_tier BETWEEN 1 AND 5),
  floor_refs               text[] NOT NULL DEFAULT '{}'::text[],
  ack_irreversible         boolean NOT NULL DEFAULT false,
  approval_ticket_id       text,                          -- FK conceptually to arifosmcp_approval_tickets.ticket_id
  human_ratifier           text,                          -- F13 — 'Muhammad Arif bin Fazil' if approved by sovereign

  -- Integrity
  payload_hash             text NOT NULL,                 -- sha256 of normalized parameters + result
  prev_receipt_hash        text,                          -- chain link to previous external action receipt

  -- Lifecycle
  metadata                 jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at               timestamptz NOT NULL DEFAULT now(),
  updated_at               timestamptz NOT NULL DEFAULT now(),
  completed_at             timestamptz,
  expires_at               timestamptz                    -- TTL — receipts auto-pruned past this (set NULL for permanent)
);

