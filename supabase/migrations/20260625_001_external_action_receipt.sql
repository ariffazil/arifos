-- ── 4. updated_at maintenance trigger ────────────────────────────────────────

CREATE OR REPLACE FUNCTION public.touch_updated_at_ear()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_ear_touch_updated_at ON public.external_action_receipt;
CREATE TRIGGER trg_ear_touch_updated_at
  BEFORE UPDATE ON public.external_action_receipt
  FOR EACH ROW EXECUTE FUNCTION public.touch_updated_at_ear();

-- ── 5. Hash integrity trigger — auto-compute payload_hash if not provided ───
-- If caller leaves payload_hash blank, compute from normalized fields.

CREATE OR REPLACE FUNCTION public.compute_ear_payload_hash()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  canonical text;
BEGIN
  IF NEW.payload_hash IS NULL OR NEW.payload_hash = '' THEN
    canonical := COALESCE(NEW.source_system,'') || '|'
              || COALESCE(NEW.action_type,'') || '|'
              || COALESCE(NEW.target,'') || '|'
              || COALESCE(NEW.result,'') || '|'
              || NEW.parameters::text || '|'
              || COALESCE(NEW.external_reference,'') || '|'
              || COALESCE(NEW.actor_id,'') || '|'
              || NEW.created_at::text;
    NEW.payload_hash := encode(digest(canonical, 'sha256'), 'hex');
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_ear_compute_hash ON public.external_action_receipt;
CREATE TRIGGER trg_ear_compute_hash
  BEFORE INSERT ON public.external_action_receipt
  FOR EACH ROW EXECUTE FUNCTION public.compute_ear_payload_hash();

